function getViewsFromLocalStorage() {
    const viewsData = localStorage.getItem('postViews');
    return viewsData ? JSON.parse(viewsData) : {};
}

function saveViewsToLocalStorage(views) {
    localStorage.setItem('postViews', JSON.stringify(views));
}

function displayPosts(filterCategory) {
    const postsContainer = document.getElementById('postsContainer');
    postsContainer.innerHTML = '';

    const storedViews = getViewsFromLocalStorage();

    const filteredPosts = filterCategory === 'all' ? posts : posts.filter(post => post.category === filterCategory);

    filteredPosts.forEach(post => {
        post.views = storedViews[post.id] || 0;

        const card = document.createElement('div');
        card.className = 'card';
        card.innerHTML = `
            <img src="${post.image}" alt="${post.title}">
            <h2>${post.title}</h2>
            <p>${post.excerpt}</p>
            <p>${post.date}</p>
            <p>Categoria: ${post.category}</p>
            <p>Views: ${post.views}</p>
        `;
        card.onclick = () => {
            window.location.href = `post.html?id=${post.id}`;
        };
        postsContainer.appendChild(card);
    });
}

function displayPost() {
    const urlParams = new URLSearchParams(window.location.search);
    const postId = parseInt(urlParams.get('id'));
    const post = posts.find(p => p.id === postId);

    if (post) {
        const storedViews = getViewsFromLocalStorage();

        post.views = (storedViews[postId] || 0) + 1;
        storedViews[postId] = post.views;

        saveViewsToLocalStorage(storedViews);
        
        document.getElementById('postContent').innerHTML = `
            <h2>${post.title}</h2>
            <p>${post.date}</p>
            <p>${post.content}</p>
            <p><strong>Categoria:</strong> ${post.category}</p>
            <p><strong>Views:</strong> ${post.views}</p>
        `;
    } else {
        document.getElementById('postContent').innerHTML = '<h2>Post não encontrado.</h2>';
    }
}

if (window.location.pathname.endsWith('post.html')) {
    displayPost();
} else {
    displayPosts('all');
}

document.getElementById('categoryFilter')?.addEventListener('change', (e) => {
    displayPosts(e.target.value);
});

function resetViews() {
    const views = getViewsFromLocalStorage();

    // Define todas as visualizações como 0
    Object.keys(views).forEach(id => {
        views[id] = 0;
    });

    saveViewsToLocalStorage(views);
    alert("Contador de visualizações reiniciado.");
    // Atualiza a página para refletir as alterações
    displayPosts('all');
}